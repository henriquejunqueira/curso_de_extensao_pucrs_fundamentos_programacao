console.log("Exemplo de operadores aritméticos");
let a = 5;
let b = 2;

// Operadores aritiméticos
let soma = a + b;
console.log(soma);

let subtracao = a - b;
console.log(subtracao);

let multiplicacao = a * b;
console.log(multiplicacao);

let divisao = a / b;
console.log(divisao);

let modulo = a % b;
console.log(modulo);

// Operadores incremento e decremento
let incremento = 3;
incremento++;
console.log(incremento);

let decremento = 7;
decremento--;
console.log(decremento);

// Operadores de atribuição
let x = 10;
x += 5;
console.log(x);

x -= 3;
console.log(x);

x *= 2;
console.log(x);

x /= 4;
console.log(x);

// Operadores de comparação
let c = 5;
let d = 3;

console.log(c == d);
console.log(c != d);
console.log(c === d);
console.log(c !== d);
console.log(c > d);
console.log(c < d);
console.log(c >= d);
console.log(c <= d);